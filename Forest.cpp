void GenForestGrid(bbRandom* bb, rnd_state* rnd_state, uint8_t* forest) {

	
	const int32_t gridSize = 10;
	//int32_t deviationChance = 40;
	//int32_t returnChance = 27;
	//int32_t branchChance = 65;
	const int32_t maxDeviationDistance = 3;
	const int32_t center = 5;
	const int32_t branchMaxLife = 4;
	const int32_t branchDieChance = 18;

	int32_t door1pos, door2pos;
	int32_t i, j;
	door1pos = bb->bbRand(rnd_state, 3, 7);
	door2pos = bb->bbRand(rnd_state, 3, 7);

	int32_t grid[gridSize * gridSize + 1] = { 0 };

	grid[door1pos] = 3;
	grid[(gridSize - 1) * gridSize + door2pos] = 3;

	int32_t pathx = door2pos;
	int32_t pathy = 1;
	int32_t dir = 1;

	grid[((gridSize - 1 - pathy) * gridSize) + pathx] = 1;

	bool deviated = false;

	while (pathy < gridSize - 4) {

		if (dir == 1) { //40 is deviation chance.
			if (Chance(bb, rnd_state, 40)) {
				dir = 2 * bb->bbRand(rnd_state, 0, 1);
				dir = TurnIfDeviating(maxDeviationDistance, pathx, center, dir);

				deviated = TurnIfDeviatingBool(maxDeviationDistance, pathx, center, dir);

				if (deviated) {
					grid[((gridSize - 1 - pathy) * gridSize) + pathx] = 1;
				}
				pathx = MoveForward(dir, pathx, pathy, 0);
				pathy = MoveForward(dir, pathx, pathy, 1);
			}
		}
		else {
			dir = TurnIfDeviating(maxDeviationDistance, pathx, center, dir);

			deviated = TurnIfDeviatingBool(maxDeviationDistance, pathx, center, dir);
			//                            27 is returnChance.
			if (deviated || Chance(bb, rnd_state, 27)) {
				dir = 1;
			}

			pathx = MoveForward(dir, pathx, pathy, 0);
			pathy = MoveForward(dir, pathx, pathy, 1);

			if (dir == 1) {
				grid[((gridSize - 1 - pathy) * gridSize) + pathx] = 1;
				pathx = MoveForward(dir, pathx, pathy, 0);
				pathy = MoveForward(dir, pathx, pathy, 1);
			}
		}
		grid[((gridSize - 1 - pathy) * gridSize) + pathx] = 1;
	}
	dir = 1;

	while (pathy < gridSize - 2) {
		pathx = MoveForward(dir, pathx, pathy, 0);
		pathy = MoveForward(dir, pathx, pathy, 1);
		grid[((gridSize - 1 - pathy) * gridSize) + pathx] = 1;
 	}

	if (pathx != door1pos) {
		dir = 0;
		if (door1pos > pathx) {
			dir = 2;
		}
		while (pathx != door1pos) {
			pathx = MoveForward(dir, pathx, pathy, 0);
			pathy = MoveForward(dir, pathx, pathy, 1);
			grid[((gridSize - 1 - pathy) * gridSize) + pathx] = 1;
		}
	}

	int32_t newy, tempy, newx;
	int32_t branchType, branchPos;
		
	newy = -3;
	while (newy < gridSize - 6) {
		newy += 4;
		tempy = newy; //65 is branchChance.
		if (Chance(bb, rnd_state, 65)) {
			branchType = -1;
			//int32_t cobbleChance = 0;

			if (Chance(bb, rnd_state, 0)) {
				branchType = -2;
			}

			branchPos = 2 * bb->bbRand(rnd_state, 0, 1);

			int32_t leftMost = gridSize;
			int32_t rightMost = 0;
			for (i = 0; i <= gridSize; i++) {
				if (grid[((gridSize - 1 - newy) * gridSize) + i] == 1) {
					if (i < leftMost) {
						leftMost = i;
					}
					if (i > rightMost) {
						rightMost = i;
					}
				}
			}

			if (branchPos == 0) {
				newx = leftMost - 1;
			}
			else {
				newx = rightMost + 1;
			}

			if ((tempy != 0 && grid[((gridSize - 1 - tempy + 1) * gridSize) + newx] == 1) || (grid[((gridSize - 1 - tempy - 1) * gridSize) + newx] == 1)) {
				break;
			}

			grid[((gridSize - 1 - tempy) * gridSize) + newx] = branchType;
			if (branchPos == 0) {
				newx = leftMost - 2;
			}
			else {
				newx = rightMost + 2;
			}
			grid[((gridSize - 1 - tempy) * gridSize) + newx] = branchType;
				
			i = 2;
			while (i < branchMaxLife) {
				i++;
				if (Chance(bb, rnd_state, branchDieChance)) {
					break;
				}
				if (bb->bbRand(rnd_state, 0, 3) == 0) {
					if (branchPos == 0) {
						newx--;
					}
					else {
						newx++;
					}
				}
				else {
					tempy++;
				}

				int32_t n = ((gridSize - 1 - tempy + 1) * gridSize) + newx;
				if (n < gridSize - 1) {
					if (tempy != 0 && grid[n] == 1) {
						break;
					}
				}
				n = ((gridSize - 1 - tempy - 1) * gridSize) + newx;
				if (n > 0) {
					if (grid[n] == 1) {
						break;
					}
				}

				grid[((gridSize - 1 - tempy) * gridSize) + newx] = branchType;
				if (tempy >= gridSize - 2) {
					break;
				}
			}
		}
	}

	for (i = 0; i <= gridSize - 1; i++) {
		for (j = 0; j <= gridSize - 1; j++) {
			if (grid[(i * gridSize) + j] == -1) {
				grid[(i * gridSize) + j] = 1;
			}
			else if (grid[(i * gridSize) + j] == -2) {
				grid[(i * gridSize) + j] = 1;
			}
		}
	}

	bool itemPlaced[4] = { false };
	for (int32_t tx = 1; tx <= gridSize - 1; tx++) {
		for (int32_t ty = 1; ty <= gridSize - 1; ty++) {
			if (grid[ty * gridSize + tx] == 1) {
				int32_t tileType = 0;
				bool hasHorConnection = false;
				bool hasVertConnection = false;
				if (tx + 1 < gridSize) {
					tileType = grid[(ty * gridSize) + tx + 1] > 0 ? 1 : 0;
					hasHorConnection = grid[(ty * gridSize) + tx + 1] > 0;
				}
				if (tx - 1 >= 0) {
					tileType += grid[(ty * gridSize) + tx - 1] > 0 ? 1 : 0;
					hasHorConnection = hasHorConnection || grid[(ty * gridSize) + tx - 1] > 0;
				}
				if (ty + 1 < gridSize) {
					tileType += grid[((ty + 1) * gridSize) + tx] > 0 ? 1 : 0;
					hasVertConnection = grid[((ty + 1) * gridSize) + tx] > 0;
				}
				if (ty - 1 >= 0) {
					tileType += grid[((ty - 1) * gridSize) + tx] > 0 ? 1 : 0;
					hasVertConnection = hasVertConnection || grid[((ty - 1) * gridSize) + tx] > 0;
				}

				if (tileType > 2) {
					tileType++;
				}
				else if (tileType == 2 && hasHorConnection && hasVertConnection) {
					tileType = ROOM2C;
				}

				if (tileType > 0) {
					if (ty % 3 == 2 && !itemPlaced[ty / 3]) {
						itemPlaced[ty / 3] = true;
						CreateItem(bb, rnd_state);					
					}

					int32_t amount = 676;
					#pragma unroll
					for (int32_t index = 0; index < amount; index++) {
						int32_t red = GetForestData(forest, tileType, index);

						if (red > bb->bbRand(rnd_state, 100, 260)) {
							int32_t roll = bb->bbRand(rnd_state, 0, 7);

							if (roll < 7) {
								bb->bbRnd(rnd_state, 0.25, 0.4);

								#pragma unroll
								for (i = 0; i <= 3; i++) {
									bb->bbRnd(rnd_state, -20, 20);
								}

								bb->bbRnd(rnd_state, 3.0, 3.2);
								bb->bbRnd(rnd_state, 5, 5);
								bb->bbRnd(rnd_state, 0, 360.0);								
							}

							if (roll == 7) {
								bb->bbRnd(rnd_state, 1, 2);
								bb->bbRnd(rnd_state, 0, 360.0);
							}
						}

					}
				}
			}
		}
	}



}

int32_t TurnIfDeviating(int32_t maxDeviationDistance, int32_t pathx, int32_t center, int32_t dir) {
	if ((dir == 0 && (center - pathx) >= maxDeviationDistance) || (dir == 2 && (center-pathx) <= -maxDeviationDistance)) {
		dir = (dir + 2) % 4;
	}

	return dir;
}

bool TurnIfDeviatingBool(int32_t maxDeviationDistance, int32_t pathx, int32_t center, int32_t dir) {
	return (dir == 0 && (center - pathx) >= maxDeviationDistance) || (dir == 2 && (center-pathx) <= -maxDeviationDistance);
}

int32_t MoveForward(int32_t dir, int32_t pathx, int32_t pathy, int32_t rv) {
	if (dir == 1) {
		if (rv == 0) {
			return pathx;
		}
		else {
			return pathy + 1;
		}
	}

	if (rv == 0) {
		return pathx - 1 + dir;
	}
	else {
		return pathy;
	}
}

bool Chance(bbRandom* bb, rnd_state* rnd_state, int32_t prob) {
	return bb->bbRand(rnd_state, 0, 100) <= prob;
}

constexpr uint8_t GetForestData(uint8_t* f, int32_t tileType, int32_t index) {
	//This is how many values we read from the forest images, per image.
	//There are 5 of these images, so 3380 values total.

	//Do this so the index is synced with the array.
	tileType--;

	//676 because that is the amount of values per image.
	index += tileType * 676;

	return f[index];
}

